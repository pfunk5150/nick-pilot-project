#!/bin/bash
# scripts/init-enhanced-session.sh
# Enhanced session initialization with workflow type support

WORKFLOW_TYPE=$1
TOPIC=$2
DATE=$(date +%Y-%m-%d)

if [ -z "$WORKFLOW_TYPE" ] || [ -z "$TOPIC" ]; then
    echo "Usage: $0 <workflow_type> <topic>"
    echo "Workflow types: session, experimental, enhancement, template, client"
    exit 1
fi

case $WORKFLOW_TYPE in
    "session")
        BRANCH_NAME="feature/session-${DATE}-${TOPIC}"
        CONTEXT_TEMPLATE="session_continuation"
        echo "ðŸŽ¯ Creating AI session workflow for structured engagement"
        ;;
    "experimental")
        BRANCH_NAME="experimental/${TOPIC}"
        CONTEXT_TEMPLATE="experimental_exploration"
        echo "ðŸ§ª Creating experimental workflow for isolated exploration"
        ;;
    "enhancement")
        BRANCH_NAME="enhancement/${TOPIC}"
        CONTEXT_TEMPLATE="apm_enhancement"
        echo "ðŸš€ Creating enhancement workflow for APM framework improvements"
        ;;
    "template")
        BRANCH_NAME="template/${TOPIC}"
        CONTEXT_TEMPLATE="template_development"
        echo "ðŸ“š Creating template workflow for AI consulting pattern development"
        ;;
    "client")
        BRANCH_NAME="client/${TOPIC}"
        CONTEXT_TEMPLATE="client_customization"
        echo "ðŸŽ¯ Creating client workflow for engagement-specific customizations"
        ;;
    *)
        echo "âŒ Error: Workflow type must be: session, experimental, enhancement, template, or client"
        exit 1
        ;;
esac

echo "ðŸš€ Creating ${WORKFLOW_TYPE} workflow: ${BRANCH_NAME}"

